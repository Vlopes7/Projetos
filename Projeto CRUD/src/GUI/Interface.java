    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente cliente = new Cliente();
        cliente.setNome(jTextField3.getText());
        cliente.setCpf(jFormattedTextField1.getText());
        cliente.setEndereco(jTextField4.getText());
        cliente.setData(jFormattedTextField3.getText());
        cliente.setTelefone(jFormattedTextField2.getText());
        cliente.setEmail(jTextField2.getText());
        
        Verificacoes verif = new Verificacoes();
        ClienteDAO dao = new ClienteDAO();
        
        if (jTextField3.getText().isEmpty() || 
            jFormattedTextField1.getText().isEmpty() || 
            jFormattedTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios.");
        }else{
            boolean cpfV = verif.verifCPF(jFormattedTextField1.getText());
            if(cpfV == false){
                JOptionPane.showMessageDialog(null, "CPF inválido");
                jFormattedTextField1.setText("");
            }else if(verif.CPFSQL(jFormattedTextField1.getText()) == false){
                if(jTextField2.getText().matches("^[\\w\\.-]+@[\\w\\.-]+\\.[a-zA-Z]{2,}$") == true){
                    if(verif.dataNasc(jFormattedTextField3.getText()) == true){
                        dao.adicionar(cliente);
                        JOptionPane.showMessageDialog(null, "Cliente " + jTextField3.getText() + " cadastrado com sucesso");
                    }else{
                        JOptionPane.showMessageDialog(null, "Formato de data de nascimento inválido! Use YYYY-MM-DD");
                        jFormattedTextField3.setText("");
                    }
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Email inválido!");
                    jTextField2.setText("");
                }
            }else{
                JOptionPane.showMessageDialog(null, "O cpf digitado já está em uso, digite outro");
                jFormattedTextField1.setText("");
            }
        }
   
    }//GEN-LAST:event_jButton1ActionPerformed

