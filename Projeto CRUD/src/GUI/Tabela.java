    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Verificacoes verif = new Verificacoes();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int rowCount = model.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            int id = (int) model.getValueAt(i, 0); 
            String nome = (String) model.getValueAt(i, 1); 
            String cpf = (String) model.getValueAt(i, 2); 
            String telefone = (String) model.getValueAt(i, 3);
            String endereco = (String) model.getValueAt(i, 4); 
            String data = (String) model.getValueAt(i, 5); 
            String email = (String) model.getValueAt(i, 6);

            if (!verif.dataNasc(data)) {
                JOptionPane.showMessageDialog(null, "Data de nascimento inválida na linha " + (i + 1));
                continue; 
            }

        verif.atualizarBancoDeDados(id, nome, cpf, telefone, endereco, data, email);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

