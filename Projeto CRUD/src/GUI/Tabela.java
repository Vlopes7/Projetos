    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(jTextPane1.getText().isEmpty() && jFormattedTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite pelo menos um dado!");
        }else{
            Verificacoes verif = new Verificacoes();
            String nome = jTextPane1.getText();
            String cpf = jFormattedTextField1.getText();
            
            if(verif.CPFSQL(cpf) == false && verif.nomeSQL(nome) == false){
                JOptionPane.showMessageDialog(null, "Usuário não encontrado");
            }else{
                verif.capturaSQL(nome, cpf, jTable1);
            }
 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

